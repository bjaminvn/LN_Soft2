    Bill getFormForBill(){
        Bill bill = new Bill();
        bill.setMaHDP(txtMaHDP.getText());
//        if (Auth.isLogin()) {
//            User user = Auth.user;
//            bill.setMaNV(user.getMaNV());
//        }
        bill.setTongTien(Float.parseFloat(txtTongTien.getText()));
        bill.setMaNV(lblMaNV.getText());
        bill.setMaKH(cbxKhachHang.getSelectedItem().toString());
        
        return bill;
    }
    
    void addBill(){
        Bill bill = getFormForBill();
        try {
            billDAO.insert(bill);
        } catch (Exception e) {
            e.printStackTrace();
            MsgBox.alert(this, "Loi khong the them hoa don!");
        }
    }
    
    BillRoom getForm(){
        BillRoom billRoom = new BillRoom();
        billRoom.setMaHDP(txtMaHDP.getText());
        billRoom.setMaKH(cbxKhachHang.getSelectedItem().toString());
        billRoom.setMaNV(lblMaNV.getText());
        billRoom.setNgayDen(XDate.toDate(txtNgayDen.getText(), "yyyy-MM-dd"));
        billRoom.setNgayDi(XDate.toDate(txtNgayDi.getText(), "yyyy-MM-dd"));
        billRoom.setSoPhong(Integer.parseInt(cbxRoom.getSelectedItem().toString()));
        billRoom.setTongtien(Float.parseFloat(txtTongTien.getText()));
        return billRoom;
    } 
    
    void insert(){
        BillRoom billRoom = getForm();
        try {
            billRoomDAO.insert(billRoom);
            MsgBox.alert(this, "Thêm hóa đơn thành công!");
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }
