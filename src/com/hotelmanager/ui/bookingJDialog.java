    void fillCbxRoom(){
        DefaultComboBoxModel model = (DefaultComboBoxModel) cbxRoom.getModel();
        model.removeAllElements();
        try {
            List<Room> list = roomDAO.selectAll();
            for (Room room : list) {
                model.addElement(room);
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }
    
    void fillCbxClient(){
        DefaultComboBoxModel model = (DefaultComboBoxModel) cbxKhachHang.getModel();
        model.removeAllElements();
        try {
            List<Client> list = clientDAO.selectAll();
            for (Client client : list) {
                model.addElement(client);
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }
    
    void setForm(){
        BillRoom model = billRoomDAO.selectByID(txtMaHDP.getText());
        txtMaHDP.setText(model.getMaHDP());
        cbxKhachHang.getEditor().setItem("123");
        if (model.getMaKH().equals(cbxKhachHang.getSelectedItem())) {
            
        }
        txtNgayDen.setText(XDate.toString(model.getNgayDen(), "dd-MM-yyyy"));
        txtNgayDi.setText(XDate.toString(model.getNgayDi(), "dd-MM-yyyy"));
        txtTongTien.setText(String.valueOf(model.getTongtien()));
    }
